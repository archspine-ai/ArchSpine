// ---------------------------------------------------------------------------
// JSON envelope types
// ---------------------------------------------------------------------------

interface BriefingEntryPoint {
  entrypoint: string;
  summary: string;
}

interface BriefingModule {
  id: string;
  layer: string;
  fanIn: number;
  fanOut: number;
  violationCount: number;
  direction: string;
  inCycle: boolean;
}

interface BriefingConstraint {
  ruleId: string;
  title: string;
  severity: string;
  scope: string[];
  summary: string;
  rationale: string | null;
}

interface BriefingHealthSummary {
  /** Non-compliant dependency edges in the module graph. */
  edgeViolations: number;
  cycleCount: number;
  deadCodeCount: number;
  hubCount: number;
  uniqueCycleParticipants: number;
}

interface AgentBriefingData {
  project: { name: string };
  techStack: Record<string, { extensions: string[]; status: string }> | null;
  entryPoints: Record<string, BriefingEntryPoint[]>;
  moduleTopology: BriefingModule[];
  architectureConstraints: BriefingConstraint[];
  healthSummary: BriefingHealthSummary;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function escapeMarkdownTableCell(value: string): string {
  return value.replace(/\|/g, '\\|').replace(/\n/g, ' ');
}

function escapeMarkdownInline(value: string): string {
  return value.replace(/`/g, '\\`');
}

function classifyDependencyDirection(node: KnowledgeGraphNode): string {
  if (node.fanIn === 0 && node.fanOut === 0) {
    return 'Isolated';
  }
  if (node.fanIn >= node.fanOut * 2) {
    return 'Hub (served)';
  }
  if (node.fanOut >= node.fanIn * 2) {
    return 'Consumer';
  }
  return 'Balanced';
}

function readJsonFile<T>(filePath: string, runtimeIO?: ViewContext['runtimeIO']): T | null {
  try {
    if (!fs.existsSync(filePath)) {
      return null;
    }
    const raw = fs.readFileSync(filePath, 'utf-8');
    return JSON.parse(raw) as T;
  } catch (error) {
    runtimeIO?.warn(
      `[AgentBriefing] Failed to read ${filePath}: ${error instanceof Error ? error.message : String(error)}`,
    );
    return null;
  }
}

