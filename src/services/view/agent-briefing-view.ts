async function deriveAgentBriefing(ctx: ViewContext): Promise<ViewArtifact> {
  const { rootDir } = ctx;

  // Load config
  const configPath = path.join(rootDir, '.spine', 'config.json');
  const config = readJsonFile<SpineConfig>(configPath, ctx.runtimeIO);

  // Load knowledge graph
  const kgPath = path.join(rootDir, '.spine', 'view', 'data', 'knowledge-graph.json');
  const kg = readJsonFile<KnowledgeGraph>(kgPath, ctx.runtimeIO);

  // Load diagnostics
  const cyclesPath = path.join(rootDir, '.spine', 'view', 'data', 'diagnostics', 'cycles.json');
  const deadCodePath = path.join(
    rootDir,
    '.spine',
    'view',
    'data',
    'diagnostics',
    'dead-code.json',
  );
  const hubsPath = path.join(rootDir, '.spine', 'view', 'data', 'diagnostics', 'hubs.json');

  const cycles = readJsonFile<CycleReport[]>(cyclesPath, ctx.runtimeIO);
  const deadCode = readJsonFile<DeadCodeReport[]>(deadCodePath, ctx.runtimeIO);
  const hubs = readJsonFile<HubReport[]>(hubsPath, ctx.runtimeIO);

  // Compute cycle participant set for topology annotations
  const cycleNodes = new Set<string>();
  if (cycles) {
    for (const cycle of cycles) {
      for (const nodeId of cycle.nodes) {
        cycleNodes.add(nodeId);
      }
    }
  }

  // Build each section
  const projectName = config?.project?.name || path.basename(rootDir);

  const sections: string[] = [];

  sections.push(`# ${escapeMarkdownInline(projectName)} — Agent Briefing`);
  sections.push('');

  sections.push('## Project Definition');
  sections.push('');
  sections.push(buildProjectDefinition(config, rootDir));
  sections.push('');

  sections.push('## Tech Stack');
  sections.push('');
  sections.push(buildTechStack(rootDir, ctx.runtimeIO));
  sections.push('');

  sections.push('## Entry Point Map');
  sections.push('');
  sections.push(buildEntryPoints(rootDir, ctx.runtimeIO));
  sections.push('');

  sections.push('## Module Topology');
  sections.push('');
  sections.push(buildModuleTopology(rootDir, cycleNodes, ctx.runtimeIO));
  sections.push('');

  sections.push('## Architecture Constraints');
  sections.push('');
  sections.push(buildArchitectureConstraints(rootDir, ctx.runtimeIO));
  sections.push('');

  sections.push('## Health Summary');
  sections.push('');
  sections.push(buildHealthSummary(rootDir, kg, cycles, deadCode, hubs));
  sections.push('');

  sections.push('');
  sections.push('---');
  sections.push(
    `*Generated at ${new Date().toISOString()} by ArchSpine agent-briefing view producer.*`,
  );

  const markdown = sections.join('\n');

  // ---------------------------------------------------------------------------
  // Build structured JSON
  // ---------------------------------------------------------------------------

  const entryPointsJson: Record<string, BriefingEntryPoint[]> = {};
  const publicSurfacePath = path.join(rootDir, '.spine', 'view', 'data', 'public-surface.json');
  const psRaw = readJsonFile<{
    items: Array<{ entrypoint: string; kind: string; summary: string }>;
  }>(publicSurfacePath, ctx.runtimeIO);
  if (psRaw?.items) {
    for (const item of psRaw.items) {
      const list = entryPointsJson[item.kind] || [];
      list.push({ entrypoint: item.entrypoint, summary: item.summary });
      entryPointsJson[item.kind] = list;
    }
  }

  const topologyJson: BriefingModule[] = (kg?.nodes || []).map((node) => ({
    id: node.id,
    layer: node.layer,
    fanIn: node.fanIn,
    fanOut: node.fanOut,
    violationCount: node.violationCount,
    direction: classifyDependencyDirection(node),
    inCycle: cycleNodes.has(node.id),
  }));

  const rulesDir = path.join(rootDir, '.spine', 'rules');
  const constraintsJson: BriefingConstraint[] = [];
  if (fs.existsSync(rulesDir)) {
    const loadedRules = loadRulesFromDir(rulesDir);
    for (const { rule } of loadedRules) {
      constraintsJson.push({
        ruleId: rule.ruleId,
        title: rule.title,
        severity: rule.severity,
        scope: rule.appliesTo,
        summary: rule.summary,
        rationale: rule.rationale ?? null,
      });
    }
  }

  const techStackJson = readJsonFile<{
    languages: Record<string, { extensions: string[]; status: string }>;
  }>(path.join(rootDir, '.spine', 'languages.json'));

  const envelope: IViewArtifactEnvelope<AgentBriefingData> = {
    schemaVersion: '2.0.0',
    generatedAt: new Date().toISOString(),
    viewType: 'agent-briefing',
    summary: `ArchSpine agent briefing for ${escapeMarkdownInline(projectName)}. ${kg?.nodes?.length ?? 0} modules, ${kg?.edges?.length ?? 0} edges, ${constraintsJson.length} rules.`,
    items: [
      {
        project: {
          name: projectName,
        },
        techStack: techStackJson?.languages ?? null,
        entryPoints: entryPointsJson,
        moduleTopology: topologyJson,
        architectureConstraints: constraintsJson,
        healthSummary: {
          edgeViolations: kg ? kg.edges.filter((e) => !e.compliant).length : 0,
          cycleCount: cycles?.length || 0,
          deadCodeCount: deadCode?.length || 0,
          hubCount: hubs?.length || 0,
          uniqueCycleParticipants: cycleNodes.size,
        },
      },
    ],
    _quality: {
      nodeCount: kg?.nodes?.length ?? 0,
      edgeCount: kg?.edges?.length ?? 0,
      cycleCount: cycles?.length ?? 0,
      deadCodeCount: deadCode?.length ?? 0,
      hubCount: hubs?.length ?? 0,
      totalCandidatesEvaluated: constraintsJson.length,
      selectionRate: `${topologyJson.length} modules, ${constraintsJson.length} rules`,
    },
  };

  ctx.outputManager.saveView('agent-briefing.json', envelope);
  ctx.outputManager.saveViewMarkdown('agent-briefing.md', markdown);

  ctx.runtimeIO?.info(`[View] Wrote agent briefing for project "${projectName}".`);

  const hasKg = kg !== null;
  const hasDiagnostics = cycles !== null && deadCode !== null && hubs !== null;

  return {
    viewType: 'agent-briefing',
    generated: true,
    generatedAt: new Date().toISOString(),
    metrics: {
      moduleCount: kg?.nodes?.length || 0,
      edgeCount: kg?.edges?.length || 0,
      violationCount: kg ? kg.edges.filter((e) => !e.compliant).length : 0,
      cycleCount: cycles?.length || 0,
      deadCodeCount: deadCode?.length || 0,
      hubCount: hubs?.length || 0,
      hasKnowledgeGraph: hasKg ? 1 : 0,
      hasDiagnostics: hasDiagnostics ? 1 : 0,
    },
  };
}

export const agentBriefingProducer: ViewProducer = {
  async derive(ctx: ViewContext): Promise<ViewArtifact> {
    return deriveAgentBriefing(ctx);
  },
};
